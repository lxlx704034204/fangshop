package com.fang.fangshop.been;

import com.fang.fangshop.core.been.*;

import java.io.Serializable;
import java.util.List;

public class OrderModel extends Order implements Serializable
{
	private static final long serialVersionUID = 1L;
    //属性
	private List<OrderDetail> orderDetails;
	private List<OrderLog> orderLogs;
	private List<OrderPay> orderPays;
	private OrderShip orderShip;
	//查询条件
	
	public List<OrderDetail> getOrderDetails() {
		return orderDetails;
	}
	public void setOrderDetails(List<OrderDetail> orderDetails) {
		this.orderDetails = orderDetails;
	}
	public List<OrderLog> getOrderLogs() {
		return orderLogs;
	}
	public void setOrderLogs(List<OrderLog> orderLogs) {
		this.orderLogs = orderLogs;
	}
	public List<OrderPay> getOrderPays() {
		return orderPays;
	}
	public void setOrderPays(List<OrderPay> orderPays) {
		this.orderPays = orderPays;
	}
	public OrderShip getOrderShip() {
		return orderShip;
	}
	public void setOrderShip(OrderShip orderShip) {
		this.orderShip = orderShip;
	}
}
